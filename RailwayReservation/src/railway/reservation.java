    private void txtticketsKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtticketsKeyPressed
        // TODO add your handling code here:
        int price,ticket_no,tot;
        
        if(evt.getKeyCode()== KeyEvent.VK_ENTER)
        {
            
            price = Integer.parseInt(txtprice.getText());
            ticket_no = Integer.parseInt(txttickets.getText());
            tot = price * ticket_no;
            
            txttotal.setText(String.valueOf(tot));
            
        }
    }//GEN-LAST:event_txtticketsKeyPressed

