    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String passno = pno.getText();
        String startplace= depplace.getSelectedItem().toString();
        String endplace = arrplace.getSelectedItem().toString();
        String trainnum = txttrain.getText();
        String trainname = txtname.getText();
        String fare = txtprice.getText();
        String journey = txtdate.getText();
        String ticket = txttickets.getText();
        String total = txttotal.getText();
        
       
        try
        {
            pst = con.prepareStatement("insert into reservation(pno,departure,arrival,trainno,trainname,price,date,ticketno,totalfare)values(?,?,?,?,?,?,?,?,?)");
            pst.setString(1,passno);
            pst.setString(2,startplace);
            pst.setString(3,endplace);
            pst.setString(4,trainnum);
            pst.setString(5,trainname);
            pst.setString(6,fare);
            pst.setString(7,journey);
            pst.setString(8,ticket);
            pst.setString(9,total);
            int k = pst.executeUpdate();
            if (k==1){
                JOptionPane.showMessageDialog(this,"Record added");
            }
            else{
                JOptionPane.showMessageDialog(this,"record failed");
            }
            
        } 
        catch (SQLException ex) {
            Logger.getLogger(addtrain.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

