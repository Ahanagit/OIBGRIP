    public void loadStart(){
        try{
            pst  = con.prepareStatement("select txtstart from traindetails");
            rs = pst.executeQuery();
            depplace.removeAllItems();
            while(rs.next()){
                depplace.addItem(rs.getString(1));
            }
        }catch(Exception e){
            e.printStackTrace();
        }
    }
    
    public void loadEnd(){
        try{
            pst  = con.prepareStatement("select txtdest from traindetails");
            rs = pst.executeQuery();
            arrplace.removeAllItems();
            while(rs.next()){
                arrplace.addItem(rs.getString(1));
            }
        }catch(Exception e){
            e.printStackTrace();
        }
    }
    
    public void load(){
        String splace = depplace.getSelectedItem().toString();
        String eplace = arrplace.getSelectedItem().toString();
        
        try{
            pst = con.prepareStatement("select * from traindetails where txtstart = ? and txtdest = ?");
            pst.setString(1, splace);
            pst.setString(2, eplace);
            rs  = pst.executeQuery();
            
            
        if(rs.next() == true)
        {
            txttrain.setText(rs.getString(1));
            txtname.setText(rs.getString(2));
            txtprice.setText(rs.getString(5));
        }
        else
        {
            JOptionPane.showMessageDialog(this,"Record not found");
        }
        
        }
        
        catch(Exception e){
            e.printStackTrace();
        }
        
    }
                                                                                
